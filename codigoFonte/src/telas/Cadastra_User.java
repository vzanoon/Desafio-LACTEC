    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        /* Metodo para Insercao no Banco de Dados dos Campos de Cadastro */
        
        if(validarCamposInsercao()){
            Connection con = (Connection) paramSend.con;
            String sql;
            PreparedStatement stmt;

            sql = "INSERT INTO users (nomeUser, idadeUser, telefoneUser) VALUES (?,?,?)";
            try {
                stmt = (PreparedStatement) con.prepareStatement(sql);
                stmt.setString(1,jTextFieldNome.getText());
                stmt.setString(2, jTextFieldIdade.getText());
                stmt.setString(3, jFormattedTextFieldTelefone.getText());

                if (stmt.executeUpdate() != 1) {
                    System.out.println("Erro ao inserir!");
                }
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(Cadastra_User.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButtonSalvarActionPerformed

