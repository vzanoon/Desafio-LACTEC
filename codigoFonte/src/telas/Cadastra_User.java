     /*Fun√ß√£o que valida a inser√ß√£o de campos v√°lidos!*/  
    private boolean validarCamposInsercao(){
      if("".equals(jTextFieldNome.getText())){  
            /*Se o campo nome for vazio emite um erro!*/  
            JOptionPane.showMessageDialog(rootPane,"Campo 'Nome' deve ser preenchido!","Aviso!",JOptionPane.WARNING_MESSAGE);
            return false;
        }else if("".equals(jTextFieldIdade.getText()) || (Integer.parseInt(jTextFieldIdade.getText()) < 0)){
            /*Se o campo idade for vazio ou menor que 0 emite um erro!*/  
            JOptionPane.showMessageDialog(rootPane,"Campo 'Idade' deve ser preenchido!","Aviso!",JOptionPane.WARNING_MESSAGE);
            return false;
        }else if("".equals(jFormattedTextFieldTelefone.getText())){
            /*Se o campo telefone for vazio emite um erro!*/  
            JOptionPane.showMessageDialog(rootPane,"Campo 'Telefone' deve ser preenchido!","Aviso!",JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

